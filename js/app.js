const DEFAULT_LANG = "en";
const enCache = {};

function applyLang(lang) {
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (lang === "es" && translations.es[key] != null) {
      el.innerHTML = translations.es[key];
    } else if (enCache[key] != null) {
      el.innerHTML = enCache[key];
    }
  });

  document.documentElement.lang = lang;
  document.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.classList.toggle("active", btn.getAttribute("data-lang") === lang);
  });

  try {
    localStorage.setItem("lang", lang);
  } catch (e) {
    /* localStorage unavailable (e.g. file:// in some browsers) — ignore */
  }
}

document.addEventListener("DOMContentLoaded", () => {
  // Cache the English content already in the DOM.
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    enCache[el.getAttribute("data-i18n")] = el.innerHTML;
  });

  document.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.addEventListener("click", () => applyLang(btn.getAttribute("data-lang")));
  });

  let saved = DEFAULT_LANG;
  try {
    saved = localStorage.getItem("lang") || DEFAULT_LANG;
  } catch (e) {
    /* ignore */
  }
  applyLang(saved);
});
